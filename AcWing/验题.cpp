void solve() {
    int n;
    cin >> n;
    while(cin >> n) {
        vector<ll> dp(n + 1);
        dp[0] = 1;
        dp[1] = 1;
        for (int i = 2; i <= n; ++i) {
            for (int j = 0; j < i; ++j) {
                dp[i] += dp[j] * dp[i - j - 1];
            }
        }
        cout << dp[n] << endl;
    }
}

