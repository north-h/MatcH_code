const int N = 100010;
const int INF = 0x3f3f3f3f;

using namespace std;

void solve() {
    int x, y, n; cin >> x >> y >> n;
    int ans = INF;
    for (int i = 0; i <= n; i ++) {
        for (int j = 0; j <= n / 3; j ++) {
            if (i + j * 3 == n) ans = min(x * i + j * y, ans);
        }
    }
    cout << ans << '\n';
}

int32_t main() {
    ios::sync_with_stdio(false), cin.tie(nullptr);
    int h_h = 1;
    // cin >> h_h;
    while (h_h--)solve();
    return 0;
}